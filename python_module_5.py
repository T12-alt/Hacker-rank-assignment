## Question 24: Alphabet Rangoli
def print_rangoli(size):
    import string

    alpha = string.ascii_lowercase
    width = 4 * size - 3

    for i in range(size - 1, -size, -1):
        x = abs(i)
        row = "-".join(alpha[size-1:x:-1] + alpha[x:size])
        print(row.center(width, "-"))

n = int(input())
print_rangoli(n)

