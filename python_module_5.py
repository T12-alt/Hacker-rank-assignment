## Question 26: The Minion Game
def minion_game(string):
    vowels = "AEIOU"
    kevin = 0
    stuart = 0
    n = len(string)

    for i in range(n):
        if string[i] in vowels:
            kevin += n - i
        else:
            stuart += n - i

    if kevin > stuart:
        print("Kevin", kevin)
    elif stuart > kevin:
        print("Stuart", stuart)
    else:
        print("Draw")

s = input()
minion_game(s)

## Question 27: Merge the Tools
def merge_the_tools(string, k):
    for i in range(0, len(string), k):
        part = string[i:i+k]

        unique = ""
        for char in part:
            if char not in unique:
                unique += char

        print(unique)

